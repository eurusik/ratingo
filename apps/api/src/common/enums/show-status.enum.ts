/**
 * TMDB Show Status values
 * @see https://developer.themoviedb.org/docs/
 * 
 * Discover API filter values:
 * - 0: Returning Series
 * - 1: Planned
 * - 2: In Production
 * - 3: Ended
 * - 4: Cancelled
 * - 5: Pilot
 */
export enum ShowStatus {
  RETURNING_SERIES = 'Returning Series',
  PLANNED = 'Planned',
  IN_PRODUCTION = 'In Production',
  ENDED = 'Ended',
  CANCELED = 'Canceled',
  PILOT = 'Pilot',
}
